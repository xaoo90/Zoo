CREATE TABLE employee(
	idEmployee NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT employee_pk PRIMARY KEY,
	login VARCHAR2(30) CONSTRAINT employee_login_nn NOT NULL CONSTRAINT employee_login_uq UNIQUE,
	password VARCHAR2(30) CONSTRAINT employee_password_nn NOT NULL,
	firstName VARCHAR2(30) CONSTRAINT employee_firstName_nn NOT NULL,
	lastName VARCHAR2(30) CONSTRAINT employee_lastName_nn NOT NULL,
	position VARCHAR2(20) CONSTRAINT employee_position_nn NOT NULL,
	employmentDate DATE CONSTRAINT employee_employmentDate_nn NOT NULL,
	endingDate DATE,
	salary NUMBER(8,2) CONSTRAINT employee_salary_nn NOT NULL,
	bankAccountNumber VARCHAR2(26) CONSTRAINT employee_bankAccountNumber_nn NOT NULL,
	holiday NUMBER(3) CONSTRAINT employee_holiday_nn NOT NULL,
	email VARCHAR2(30) CONSTRAINT employee_email_nn NOT NULL,
	condition VARCHAR2(30) CONSTRAINT employee_condition_nn NOT NULL
);

CREATE TABLE holiday(
	idHoliday NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT holiday_pk PRIMARY KEY,
	employee NUMBER(5) CONSTRAINT holiday_employee_nn NOT NULL,
	startDate DATE CONSTRAINT holiday_startDate_nn NOT NULL,
	endDate DATE CONSTRAINT holiday_endDate_nn NOT NULL,
	type VARCHAR2(30) CONSTRAINT holiday_type_nn NOT NULL,
	CONSTRAINT holiday_employee_fk FOREIGN KEY(employee) REFERENCES employee(idEmployee)
);

CREATE TABLE workSchedule(
	idWorkSchedule NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT workSchedule_pk PRIMARY KEY,
	workDate DATE CONSTRAINT workSchedule_workDate_nn NOT NULL,
	workShifts VARCHAR2(30) CONSTRAINT workSchedule_workShifts_nn NOT NULL
);

CREATE TABLE employeeSchedule(
	idEmployeeSchedule NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT es_pk PRIMARY KEY,
	employee NUMBER(5) CONSTRAINT es_employee_nn NOT NULL,
	workSchedule NUMBER(5) CONSTRAINT es_workSchedule_nn NOT NULL,	
	position VARCHAR2(30) CONSTRAINT es_position_nn NOT NULL,
	CONSTRAINT es_uq UNIQUE(employee, workSchedule),
	CONSTRAINT es_employee_fk FOREIGN KEY(employee) REFERENCES employee(idEmployee),
	CONSTRAINT es_workSchedule_fk FOREIGN KEY(workSchedule) REFERENCES workSchedule(idWorkSchedule)
);

CREATE TABLE sector(
	idSector NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT sector_pk PRIMARY KEY,
	manager NUMBER(5),
	type VARCHAR2(20),
	name VARCHAR2(30) CONSTRAINT sector_name_uq UNIQUE,
	CONSTRAINT sector_manager_fk FOREIGN KEY(manager) REFERENCES employee(idEmployee)
);

CREATE TABLE cage(
	idCage NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT cage_pk PRIMARY KEY,
	sector NUMBER(5) CONSTRAINT cage_sector_nn NOT NULL,
	type VARCHAR2(30) CONSTRAINT cage_type_nn NOT NULL,
	condition VARCHAR2(30) DEFAULT '0' CONSTRAINT cage_condition_nn NOT NULL,
	space NUMBER(5) CONSTRAINT cage_space_nn NOT NULL,
	CONSTRAINT cage_sector_fk FOREIGN KEY(sector) REFERENCES sector(idSector)
);

CREATE TABLE animal(
	idAnimal NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT animal_pk PRIMARY KEY,
	cage NUMBER(5) CONSTRAINT animal_cage_nn NOT NULL,
	species VARCHAR2(30) CONSTRAINT animal_species_nn NOT NULL,
	sex VARCHAR2(20),
	description VARCHAR (255),
	birthDate DATE CONSTRAINT animal_birthDate_nn NOT NULL,
	arrivalDate DATE CONSTRAINT animal_arrivalDate_nn NOT NULL,
	source VARCHAR2(30) CONSTRAINT animal_source_nn NOT NULL,
	CONSTRAINT animal_cage_fk FOREIGN KEY(cage) REFERENCES cage(idCage)
);

CREATE TABLE medExamination(
	idMedExamination NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT medExamination_pk PRIMARY KEY,
	animal NUMBER(5) CONSTRAINT medExamination_animal_nn NOT NULL,
	veterinarian NUMBER(5) CONSTRAINT medExamination_veterinarian_nn NOT NULL,
	medDate DATE CONSTRAINT medExamination_databadania_nn NOT NULL,
	condition VARCHAR2(30) CONSTRAINT medExamination_condition_nn NOT NULL,
	description VARCHAR2(255),
	CONSTRAINT medExamination_animal_fk FOREIGN KEY(animal) REFERENCES animal(idAnimal),
	CONSTRAINT medExamination_veterinarian_fk FOREIGN KEY(veterinarian) REFERENCES employee(idEmployee)
);

CREATE TABLE warehouse(
	idWarehouse NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT warehouse_pk PRIMARY KEY,
	name VARCHAR2(30) CONSTRAINT warehouse_name_unique UNIQUE CONSTRAINT warehouse_name_nn NOT NULL
);

CREATE TABLE feed(
	idFeed NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT feed_pk PRIMARY KEY,
	warehouse NUMBER(5) CONSTRAINT feed_warehouse_nn NOT NULL,
	name VARCHAR2(30) CONSTRAINT feed_name_nn NOT NULL,
	availability NUMBER(10,2) DEFAULT 0,
	unit VARCHAR2(20),
	CONSTRAINT feed_warehouse_fk FOREIGN KEY(warehouse) REFERENCES warehouse(idWarehouse)
);

CREATE TABLE animalFeed(
	idAnimalFeed NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT af_pk PRIMARY KEY,
	animal NUMBER(5) CONSTRAINT af_animal_nn NOT NULL,
	feed NUMBER(5) CONSTRAINT af_feed_nn NOT NULL,
	portion NUMBER(5,2),
	feedTime VARCHAR2(20),
	frequency VARCHAR2(30),
	CONSTRAINT af_animal_fk FOREIGN KEY(animal) REFERENCES animal(idAnimal),
	CONSTRAINT af_feed_fk FOREIGN KEY(feed) REFERENCES feed(idFeed)
);

CREATE TABLE orderFeed(
	idOrder NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT order_pk PRIMARY KEY,
	warehousemen NUMBER(5) CONSTRAINT order_warehousemen_nn NOT NULL,
	orderDate DATE CONSTRAINT order_orderDate_nn NOT NULL,
	condition VARCHAR2(30) CONSTRAINT order_condition_nn NOT NULL,
	CONSTRAINT order_warehousemen_fk FOREIGN KEY(warehousemen) REFERENCES employee(idEmployee)
);

CREATE TABLE orderPosition(
	idOrderPosition NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT op_pk PRIMARY KEY,	
	feed NUMBER(5) CONSTRAINT op_feed_nn NOT NULL,
	orderFeed NUMBER(5,2) CONSTRAINT op_order_nn NOT NULL,
	amount NUMBER(7,2) CONSTRAINT op_amount_nn NOT NULL,
	price NUMBER(5,2),
	CONSTRAINT op_feed_fk FOREIGN KEY(feed) REFERENCES feed(idFeed),
	CONSTRAINT op_order_fk FOREIGN KEY(orderFeed) REFERENCES orderFeed(idOrder)
);

CREATE TABLE ticketType(
	idTicketType NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT ticketType_pk PRIMARY KEY,
	name VARCHAR2(30) CONSTRAINT ticketType_name_nn NOT NULL CONSTRAINT ticketType_name_uq UNIQUE,
	price NUMBER(5,2) CONSTRAINT ticketType_price_nn NOT NULL
);

CREATE TABLE ticket(
	idTicket NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY NOCACHE CONSTRAINT ticket_pk PRIMARY KEY,
	ticketType NUMBER(5) CONSTRAINT ticket_ticketType_nn NOT NULL,
